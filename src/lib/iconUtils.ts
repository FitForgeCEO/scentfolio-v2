/**
 * Get the typographic glyph for a Material icon name.
 * Falls back to a generic editorial diamond if unmapped.
 */
export function getIconChar(iconName: string | undefined | null): string {
  if (!iconName) return '\u25C8'
  return ICON_MAP[iconName] || '\u25C8'
}
